    private void cargarCombo() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        list = g.llenarComboCiudad();

        for (Ciudad art : list) {
            model.addElement(art.getDescripcion());

        }
        cboCiudad.setModel(model);
    }



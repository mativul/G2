    private int devolverid(String est) {
        int var = 0;
        for (Ciudad ciu : list) {
            if (ciu.getDescripcion().equals(est)) {
                var = ciu.getId_ciudad();
            }
        }
        return var;
    }

    private void limpiarCampos() {
        txtApellidoArtista.setText("");
        txtNombreArtista.setText("");
    }

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtNombreArtista.getText().equals("") && !txtApellidoArtista.getText().equals("")) {
            String nombre = txtNombreArtista.getText();
            String apellido = txtApellidoArtista.getText();

            String descripcion = (String) cboCiudad.getSelectedItem();
            int ciudad = devolverid(descripcion);

            Artista a = new Artista(nombre, apellido, ciudad);

            g.RegistrarAutor(a);

            JOptionPane.showMessageDialog(this, "Artista Cargado Con Exito");
        } else {
            JOptionPane.showMessageDialog(this, "Complete los campos");
        }
        limpiarCampos();

    }//GEN-LAST:event_btnAgregarActionPerformed


    private void cargarlista() {
        ArrayList<Restauracion> lista = db.mostarResta();
        DefaultTableModel dtm = new DefaultTableModel();
        dtm = new DefaultTableModel();

        String[] titulos = new String[]{"Id", "restauracionObra", "restauracion", "nombreObra", "nombre", "apellido"};
        dtm.setColumnIdentifiers(titulos);
        tableres.setModel(dtm);
        // idGasto,descripcion,importe,nombre,maximoAutorizado
        for (Restauracion lg : lista) {
            Object[] row = {lg.getId(), lg.getRestauracionObra(), lg.getRestauracion(), lg.getNombreObra(), lg.getNombre(), lg.getApellido()};
            dtm.addRow(row);
        }
        tableres.setModel(dtm);
    }

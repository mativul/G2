    private void tableresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableresMouseClicked

        String v = "";
        int index = tableres.getSelectedRow();
        TableModel model = tableres.getModel();
        value = Integer.parseInt(model.getValueAt(index, 0).toString());

        v = db.versiono(value);

        if (v.equals("si")) {
            rbtNo.setSelected(false);
            rbtSi.setSelected(true);

            proceso = 1;

        } else {

            rbtNo.setSelected(true);
            rbtSi.setSelected(false);
            proceso = 0;
        }


    }//GEN-LAST:event_tableresMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String estado = "";
        if (rbtNo.isSelected()) {
            estado = "no";
            if (value > 0) {
                db.modificarRest(value);
                cargarlista();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione Una Obra");
            }
        }
        if (rbtSi.isSelected()) {
            estado = "si";
            JOptionPane.showMessageDialog(this, "Seleccione Para no Restaurar");
            return;
        }


    }//GEN-LAST:event_btnModificarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        
        if (value > 0) {
            db.EliminarRest(value);
            JOptionPane.showMessageDialog(this, "Se Elimino con Exito");
            cargarlista();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione una Obra");
        }
    }//GEN-LAST:event_btneliminarActionPerformed


    private void cargartxt() {
        String na = listo.getNombreartista();
        cboArtista.setSelectedItem(na);
        String ge = listo.getGenero();
        cboGenero.setSelectedItem(ge);
        String des = listo.getDescripcion();
        cboDescripcion.setSelectedItem(des);
        txtAlto.setText(String.valueOf(listo.getAlto()));
        txtAncho.setText(String.valueOf(listo.getAncho()));
        txtValuacion.setText(String.valueOf(listo.getIngreso()));
        txtObra.setText(listo.getNombre());
        txtValuacion.setText(String.valueOf(listo.getValuacion()));

    }

    private void abrir() {

        ListaObras o = new ListaObras();
        o.setVisible(true);
        o.setLocationRelativeTo(null);
        o.setDefaultCloseOperation(o.EXIT_ON_CLOSE);

        this.setVisible(false);
        this.dispose();

    }

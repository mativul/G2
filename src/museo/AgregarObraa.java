    private void txtValuacionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValuacionKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();

        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar Solo numeros");
        }
        if (txtValuacion.getText().length() > 9) {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar hasta 10 caracteres");
        }
    }//GEN-LAST:event_txtValuacionKeyTyped


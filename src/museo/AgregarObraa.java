    private void cargarCombo() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        list = g.llenarCombo();

        for (Artista art : list) {
            model.addElement(art.getNombre());

        }
        cboArtista.setModel(model);
    }

    private int devolverid(String est) {
        int var = 0;
        for (Artista art : list) {
            if (art.getNombre().equals(est)) {
                var = art.getId_artista();
            }
        }
        return var;
    }

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        if (!txtAlto.getText().equals("") && !txtAncho.getText().equals("") && !txtAlto.getText().equals("") && !txtValuacion.getText().equals("") && !txtValuacion.getText().equals("") && !txtObra.getText().equals("")) {

            int alto = Integer.parseInt(txtAlto.getText());
            int ancho = Integer.parseInt(txtAncho.getText());
            String ingreso = txtValuacion.getText();
            String nombreobra = txtObra.getText();
            int valuacion = Integer.parseInt(txtValuacion.getText());
            String descripcion = (String) cboDescripcion.getSelectedItem();

            String art = (String) cboArtista.getSelectedItem();
            int artista = devolverid(art);

            String gen = (String) cboGenero.getSelectedItem();

            Obra o = new Obra(artista, nombreobra, gen, valuacion, descripcion, alto, ancho, ingreso);

            g.RegistarObras(o);

            JOptionPane.showMessageDialog(this, "Obra Cargada Con Exito");
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese Todos Los Campos");
        }


    }//GEN-LAST:event_btnAgregarActionPerformed


    private void cargarLista() {
        DefaultTableModel model = new DefaultTableModel();
        String[] column = {"id Obra", "Artista", "nombre Obra", "Genero", "valuacion", "descripcion", "Alto", "Ancho", "Ingreso"};
        model.setColumnIdentifiers(column);
        listobra = g.mostrarObra();
        for (Obra o : listobra) {
            Object[] obj = {o.getId_obra(), o.getNombreartista(), o.getNombre(), o.getGenero(), o.getValuacion(), o.getDescripcion(), o.getAlto(), o.getAncho(), o.getIngreso()};
            model.addRow(obj);
        }
        tblObras.setModel(model);
    }


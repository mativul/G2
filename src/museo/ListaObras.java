    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        Obra o1 = new Obra();
        int idPersonaAModificar = (int) tblObras.getValueAt(tblObras.getSelectedRow(), 0);
        for (Obra o : listobra) {
            if (idPersonaAModificar == o.getId_obra()) {
                o1 = new Obra(o.getId_obra(), o.getNombreartista(), o.getNombre(), o.getGenero(), o.getValuacion(), o.getDescripcion(), o.getAlto(), o.getAncho(), o.getIngreso());

            }
        }
        g.EliminarObra(o1.getId_obra());
        cargarLista();

    }//GEN-LAST:event_EliminarActionPerformed


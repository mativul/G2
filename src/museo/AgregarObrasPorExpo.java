    private void cboexpoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboexpoItemStateChanged
        String a = cboexpo.getSelectedItem().toString();

        for (ExposisionesCombo listRestauradore : listitaT) {
            if (listRestauradore.getNombre().equals(a)) {
                id = listRestauradore.getId();
            }

        }
        cargarLista(id);
        cargarListaSIn();

    }//GEN-LAST:event_cboexpoItemStateChanged


    private void cargarCombo() {

        DefaultComboBoxModel model = new DefaultComboBoxModel();

        listitaT = g.mostrarExposision();

        for (ExposisionesCombo listRestauradore : listitaT) {
            model.addElement(listRestauradore.getNombre());

        }
        cboexpo.setModel(model);

    }

    private void cargarLista(int i) {
        DefaultTableModel model = new DefaultTableModel();
        String[] column = {"Id Obra", "nombre Obra"};
        model.setColumnIdentifiers(column);
        listitaA = g.mostrarObraPorExpo(i);
        for (Obra o : listitaA) {
            Object[] obj = {o.getId_obra(), o.getNombre()};
            model.addRow(obj);
        }
        tblexpo.setModel(model);
    }

    private void cargarListaSIn() {
        DefaultTableModel model = new DefaultTableModel();
        String[] column = {"Id Obra", "nombre Obra"};
        model.setColumnIdentifiers(column);
        listitaB = g.mostrarObrasinExpo();
        for (Obra o : listitaB) {
            Object[] obj = {o.getId_obra(), o.getNombre()};
            model.addRow(obj);
        }
        tblsinexpo.setModel(model);
    }

